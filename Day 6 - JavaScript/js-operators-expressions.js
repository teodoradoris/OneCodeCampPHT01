//Arithmetic Operation
let x = 10;
let y = 5;

//Getting the sum or total '+'
let sum = x + y;
console.log("The sum is:", sum);

//difference '-'
let difference = x - y;
console.log("The difference is:", difference);

//product '*'
let product = x * y;
console.log("The product is:", product);

//quotient '*'
let quotient = x / y;
console.log("The quotient is:", quotient);

//Remainder or modulo
let remainder = x % y;
console.log("The remainder is;", remainder);

let answer = 10 + 2 * 23 / 2;
console.log("the answer is:", answer);

//Concatenation
let a = "Hello";
let b = "World";
console.log(a + " " + b);

//Assignment Expressions
let i = 25;
let j = 20;

//Assignment Expressions '+='
i +=  j;
console.log(i);

//Assignment expression '-='
i -= j;
console.log(i)
//Assignment expression '*='
i *= j;
console.log(i)
//Assignment expression '/='
i /= j;
console.log(i)
