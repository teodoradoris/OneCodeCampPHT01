//Control Flow Structure
//Looping statement

//For loop
for (let i = 1; i <= 5; i++) {
    console.log("For loop sount:", i);
}

//While loop we can control the increment
let count = 1;
while (count <= 5) {
    console.log("While loop count:", count);
    count++;
    console.log("While loop count:", count);
}

// Do while
let a = 10; 
do {
    console.log("Do while count:", a)
    a++
} while (a <= 5);
